"""Deep into BIFs"""
# Use the “list” BIF to define an empty list
l = list()
print(l)

l = [1, 2, 3]
print(l)
# Using dict BIF to define an empty dictioanry
d = dict()
print(d)
d = {'first': 1, 'Second': 2, 'Third': 3}
print(d)

# Use the “set” BIF to define an empty set
s = set()
print(s)
s = {1, 2, 3, }
print(s)

# Use the “tuple” BIF to define an empty tuple
t = tuple()
print(t)
t= (1,2,3)
print(t)
